# --- HEATMAP GENERATION FUNCTION ---
def make_gradcam_heatmap(img_array, model, last_conv_layer_name='Conv_1'):
    """Generate Grad-CAM heatmap for image visualization"""
    try:
        
        # Create a model that maps the input image to the activations of the last conv layer
        grad_model = tf.keras.models.Model(
            model.inputs, 
            [model.get_layer(last_conv_layer_name).output, model.output]
        )

        with tf.GradientTape() as tape:
            conv_outputs, predictions = grad_model(img_array)
            # Model output is a list containing the prediction tensor
            prediction_tensor = predictions[0] if isinstance(predictions, list) else predictions
            loss = prediction_tensor[0, 0]  # Get the first value

        # Compute gradients of the target class with respect to the feature map
        grads = tape.gradient(loss, conv_outputs)
        
        # Global average pooling of gradients
        pooled_grads = tf.reduce_mean(grads, axis=(0, 1, 2))
        
        # Weight the feature maps with the computed gradients
        conv_outputs = conv_outputs[0]
        heatmap = conv_outputs @ pooled_grads[..., tf.newaxis]
        heatmap = tf.squeeze(heatmap)

        # Normalize the heatmap between 0 and 1
        heatmap = tf.maximum(heatmap, 0)
        if tf.math.reduce_max(heatmap) > 0:
            heatmap = heatmap / tf.math.reduce_max(heatmap)
        
        return heatmap.numpy()
    except Exception as e:
        print(f"Error generating heatmap: {e}")
        return None

def generate_heatmap_image(image_path, model, last_conv_layer_name='Conv_1'):
    """Generate and save heatmap image for a given input image"""
    try:
        # Load and preprocess image
        img = cv2.imread(image_path)
        if img is None:
            print(f"Could not load image: {image_path}")
            return None
            
        img = cv2.resize(img, (224, 224))
        img_array = np.expand_dims(img / 255.0, axis=0)
        
        # Generate heatmap
        heatmap = make_gradcam_heatmap(img_array, model, last_conv_layer_name)
        if heatmap is None:
            return None
            
        # Resize heatmap to match image size
        heatmap = cv2.resize(heatmap, (224, 224))
        
        # Convert heatmap to 0-255 range
        heatmap = np.uint8(255 * heatmap)
        
        # Apply colormap (JET colormap for red-yellow-blue visualization)
        heatmap = cv2.applyColorMap(heatmap, cv2.COLORMAP_JET)
        
        # Superimpose heatmap on original image
        superimposed_img = cv2.addWeighted(img, 0.6, heatmap, 0.4, 0)
        
        # Save heatmap image
        base_name = os.path.splitext(image_path)[0]
        heatmap_path = f"{base_name}_heatmap.jpg"
        cv2.imwrite(heatmap_path, superimposed_img)
        
        return heatmap_path
    except Exception as e:
        print(f"Error generating heatmap image: {e}")
        return None

